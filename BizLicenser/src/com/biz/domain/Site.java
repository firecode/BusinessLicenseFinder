public class Site {

	private String link_title;
	private String description;
	private String url;
	private String category;
	private String county;
	private String state;
	private String business_type;
	private String section;
	private String resource_group_description;
	/**
	 * @return the link_title
	 */
	public String getLink_title() {
		return link_title;
	}
	/**
	 * @param link_title the link_title to set
	 */
	public void setLink_title(String link_title) {
		this.link_title = link_title;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	/**
	 * @return the category
	 */
	public String getCategory() {
		return category;
	}
	/**
	 * @param category the category to set
	 */
	public void setCategory(String category) {
		this.category = category;
	}
	/**
	 * @return the county
	 */
	public String getCounty() {
		return county;
	}
	/**
	 * @param county the county to set
	 */
	public void setCounty(String county) {
		this.county = county;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
	/**
	 * @return the business_type
	 */
	public String getBusiness_type() {
		return business_type;
	}
	/**
	 * @param business_type the business_type to set
	 */
	public void setBusiness_type(String business_type) {
		this.business_type = business_type;
	}
	/**
	 * @return the section
	 */
	public String getSection() {
		return section;
	}
	/**
	 * @param section the section to set
	 */
	public void setSection(String section) {
		this.section = section;
	}
	/**
	 * @return the resource_group_description
	 */
	public String getResource_group_description() {
		return resource_group_description;
	}
	/**
	 * @param resource_group_description the resource_group_description to set
	 */
	public void setResource_group_description(String resource_group_description) {
		this.resource_group_description = resource_group_description;
	}
	
	
}

